var color = "Green";

switch(color){

    case "yellow":
        console.log("This is yellow");
        break;
    
    case "Black":
        console.log("This is Black");
        break;
    
    case "Green":
        console.log("This is Green");
        break;
    
    case "Pink":
        console.log("This is Pink");
        break;
    
    default:
        console.log("This is default colour");
        break;

}